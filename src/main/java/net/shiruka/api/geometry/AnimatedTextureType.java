package net.shiruka.api.geometry;

/**
 * an enum to determine animated texture types.
 */
public enum AnimatedTextureType {
  /**
   * the none.
   */
  NONE,
  /**
   * the face.
   */
  FACE,
  /**
   * the body as 32x32.
   */
  BODY_32X32,
  /**
   * the body as 128x128.
   */
  BODY_128X128
}
