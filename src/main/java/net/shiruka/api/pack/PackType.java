package net.shiruka.api.pack;

/**
 * an enum class that represents resource pack types.
 */
public enum PackType {
  /**
   * the invalid.
   */
  INVALID,
  /**
   * the resources.
   */
  RESOURCES,
  /**
   * the data.
   */
  DATA,
  /**
   * the plugin.
   */
  PLUGIN,
  /**
   * the client data.
   */
  CLIENT_DATA,
  /**
   * the interface.
   */
  INTERFACE,
  /**
   * the mandatory.
   */
  MANDATORY,
  /**
   * the world template.
   */
  WORLD_TEMPLATE
}
